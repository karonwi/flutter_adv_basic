class Quiz extends StatefulWidget {
  const Quiz({super.key});

  @override
  State<Quiz> createState() {
    return _QuizState();
  }
}

class _QuizState extends State<Quiz> {
  // Widget? activeScreen;
  var activeScreen = 'start-screen';

//the inistate in run before the build occurs ,therefore setting the initial
//state of quiz from the startScreen to Question screen
  // @override
  // void initState() {
